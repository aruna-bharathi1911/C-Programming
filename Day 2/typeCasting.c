#include<stdio.h>
int main(){
    int a=5;
    printf("float %f",(float)a);    // int->float

    char b='A';
    printf("\nint %d",(int)b);   //char ->int

    int c=3673;
    printf("\nlong %ld",(long)c);  //int->long

    int d=68;
    printf("\nchar %c",(char)d);    //int->char 

    return 0;

}